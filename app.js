// GSAP Animations
function initGSAP() {
    gsap.registerPlugin(ScrollTrigger);

    // Hero 3D Parallax
    gsap.to(".hero-overlay", {
        scrollTrigger: {
            trigger: ".hero",
            start: "top top",
            end: "bottom top",
            scrub: true
        },
        y: 200,
        opacity: 0.8
    });

    gsap.from(".hero-content", {
        duration: 1.5,
        y: 100,
        opacity: 0,
        ease: "power4.out"
    });

    // Gallery Parallax
    gsap.utils.toArray(".gallery-item").forEach(item => {
        gsap.from(item, {
            scrollTrigger: {
                trigger: item,
                start: "top 90%",
                toggleActions: "play none none none"
            },
            y: 50,
            opacity: 0,
            duration: 1
        });
    });
}

