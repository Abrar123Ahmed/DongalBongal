// Language Switcher
const langSwitcher = document.getElementById('languageSwitcher');
langSwitcher.addEventListener('change', (e) => {
    const lang = e.target.value;
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === 'ur' ? 'rtl' : 'ltr';

    // Update texts
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang][key]) {
            element.textContent = translations[lang][key];
        }
    });

    // Update dynamic content
    renderDestinations(lang);

    // Reset Cost Result
    document.getElementById('calc-result').textContent = "";
});

// Cost Calculator Upgraded
const calculateBtn = document.getElementById('calculate-btn');
calculateBtn.addEventListener('click', () => {
    const days = parseInt(document.getElementById('calc-days').value) || 0;
    const accCost = parseInt(document.getElementById('calc-acc').value) || 0;
    const transportCost = parseInt(document.getElementById('calc-transport').value) || 0;
    const city = document.getElementById('calc-city')?.value || 'generic';

    // AI Logic: City Multipliers (Peak season / Popularity)
    const multipliers = { hunza: 1.5, skardu: 1.4, swat: 1.2, lahore: 1.1, generic: 1.0 };
    const cityMultiplier = multipliers[city] || 1.0;

    const foodCostPerDay = 2500; // Updated rate
    const totalCost = Math.round((accCost + transportCost + foodCostPerDay) * days * cityMultiplier);

    const lang = document.documentElement.lang;
    const resultDiv = document.getElementById('calc-result');

    if (lang === 'ur') {
        resultDiv.innerHTML = `کل متوقع اخراجات: <span style="color:var(--secondary-color)">${totalCost.toLocaleString()} PKR</span><br><small>(بشمول مقام کا اضافی فیس ${Math.round((cityMultiplier - 1) * 100)}%)</small>`;
    } else {
        resultDiv.innerHTML = `Total Estimated Cost: <span style="color:var(--secondary-color)">${totalCost.toLocaleString()} PKR</span><br><small>(Incl. Location Premium ${Math.round((cityMultiplier - 1) * 100)}%)</small>`;
    }
});

