// Modal Logic
const modal = document.getElementById('dest-modal');
const modalBody = document.getElementById('modal-body');
const closeModal = document.getElementById('close-modal');

function openModal(destId, lang) {
    const dest = destinations.find(d => d.id === destId);
    if (!dest) return;

    // Translation Labels
    const labels = {
        ur: { time: "بہترین وقت", routes: "سفر اور رسائی", hurdles: "مشکلات / خطرات" },
        en: { time: "Best Time to Visit", routes: "How to Get There", hurdles: "Challenges / Hazards" }
    };

    const l = labels[lang];
    const data = dest[lang];

    let galleryHTML = dest.gallery.map(img => `<img src="${img}" alt="Gallery image">`).join('');

    modalBody.innerHTML = `
        <img src="${dest.image}" class="modal-header-img" alt="${data.title}">
        <h2 class="modal-title">${data.title}</h2>
        <p style="margin-bottom: 20px; font-size: 1.1rem;">${data.desc}</p>
        
        <div class="modal-section">
            <h4>${l.time} <span style="font-size: 1.5rem">⏳</span></h4>
            <p>${data.best_time}</p>
        </div>
        
        <div class="modal-section">
            <h4>${l.routes} <span style="font-size: 1.5rem">🚗</span></h4>
            <p>${data.routes}</p>
        </div>
        
        <div class="modal-section" style="background: rgba(220,38,38, 0.05); border-color: #dc2626;">
            <h4 style="color: #dc2626;">${l.hurdles} <span style="font-size: 1.5rem">⚠️</span></h4>
            <p>${data.hurdles}</p>
        </div>

        <div class="modal-gallery">
            ${galleryHTML}
        </div>
    `;

    modal.style.display = 'block';
    // Small delay to allow display block to apply before adding class
    setTimeout(() => modal.classList.add('show'), 10);
    document.body.style.overflow = 'hidden'; // prevent bg scroll
}

closeModal.onclick = function () {
    modal.classList.remove('show');
    setTimeout(() => {
        modal.style.display = "none";
        document.body.style.overflow = 'auto';
    }, 300);
}

window.onclick = function (event) {
    if (event.target == modal) {
        closeModal.onclick();
    }
}

function renderDestinations(lang) {
    const gridEl = document.getElementById('destinations-grid');
    gridEl.innerHTML = '';

    destinations.forEach(dest => {
        const card = document.createElement('div');
        card.className = 'card glass-card';
        card.style.cursor = 'pointer'; // indicates it's clickable
        card.innerHTML = `
            <img src="${dest.image}" alt="${dest[lang].title}" style="width: 100%; height: 200px; object-fit: cover; border-radius: 10px; margin-bottom: 15px;">
            <h3>${dest[lang].title}</h3>
            <p>${dest[lang].desc}</p>
            <div style="margin-top: 15px; color: var(--primary-color); font-weight: bold; font-size: 0.9rem;">
                ${lang === 'ur' ? 'مزید تفصیلات دیکھیں ➔' : 'View Details ➔'}
            </div>
        `;

        card.onclick = () => openModal(dest.id, document.documentElement.lang);

        gridEl.appendChild(card);
    });
}

