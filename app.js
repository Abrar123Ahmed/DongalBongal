botToggle.addEventListener('click', () => {
    botPanel.style.display = 'flex';
    if (chatMsgs.innerHTML === "") addBotMsg(translations[document.documentElement.lang].bot_welcome);
});

closeBot.onclick = () => botPanel.style.display = 'none';

function addBotMsg(text) {
    const div = document.createElement('div');
    div.className = 'msg msg-bot';
    div.textContent = text;
    chatMsgs.appendChild(div);
    chatMsgs.scrollTop = chatMsgs.scrollHeight;
}

function addUserMsg(text) {
    const div = document.createElement('div');
    div.className = 'msg msg-user';
    div.textContent = text;
    chatMsgs.appendChild(div);
    chatMsgs.scrollTop = chatMsgs.scrollHeight;
}

sendBot.onclick = () => {
    const query = botInput.value.toLowerCase();
    if (!query) return;
    addUserMsg(botInput.value);
    botInput.value = "";

    const lang = document.documentElement.lang;
    let response = botKnowledge[lang].default;

    for (let key in botKnowledge[lang]) {
        if (query.includes(key)) {
            response = botKnowledge[lang][key];
            break;
        }
    }
    setTimeout(() => addBotMsg(response), 500);
};

